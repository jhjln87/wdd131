function init() {
    addForm.classList.add('hide');
    sortForm.classList.add('hide');
    addFormButton.setAttribute('aria-expanded', 'false');
    sortByButton.setAttribute('aria-expanded', 'false');
    sortBy = "height_in";
    listContainer.innerHTML = `<h3>Your list is empty!</h3>`;
}

